function normalizeHex(color: string) {
  const normalizedColor = color.trim().replace(/^#/u, "");

  if (normalizedColor.length === 3) {
    return normalizedColor
      .split("")
      .map((character) => `${character}${character}`)
      .join("");
  }

  return normalizedColor;
}

export function hexToHslTriplet(color: string) {
  const normalizedColor = normalizeHex(color);
  const red = Number.parseInt(normalizedColor.slice(0, 2), 16) / 255;
  const green = Number.parseInt(normalizedColor.slice(2, 4), 16) / 255;
  const blue = Number.parseInt(normalizedColor.slice(4, 6), 16) / 255;
  const maxChannel = Math.max(red, green, blue);
  const minChannel = Math.min(red, green, blue);
  const lightness = (maxChannel + minChannel) / 2;
  const delta = maxChannel - minChannel;
  let hue = 0;
  let saturation = 0;

  if (delta !== 0) {
    saturation =
      lightness > 0.5 ? delta / (2 - maxChannel - minChannel) : delta / (maxChannel + minChannel);

    switch (maxChannel) {
      case red:
        hue = (green - blue) / delta + (green < blue ? 6 : 0);
        break;
      case green:
        hue = (blue - red) / delta + 2;
        break;
      default:
        hue = (red - green) / delta + 4;
        break;
    }

    hue /= 6;
  }

  return `${Math.round(hue * 360)} ${Math.round(saturation * 100)}% ${Math.round(lightness * 100)}%`;
}

function buildThemeVariablesFromHex(input: ThemeTokenInput) {
  return buildThemeVariables(
    Object.fromEntries(
      Object.entries(input).map(([tokenName, tokenValue]) => [tokenName, hexToHslTriplet(tokenValue)]),
    ) as unknown as ThemeTokenInput,
  );
}

