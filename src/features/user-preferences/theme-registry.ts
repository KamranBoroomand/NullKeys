export const themeChoices = themeLibrary.map((theme) => theme.id) as ThemeChoice[];

export function getThemeDefinition(themeChoice?: string) {
  return themeLibrary.find((theme) => theme.id === themeChoice) ?? themeLibrary[0];
}

export function getNextThemeChoice(themeChoice: ThemeChoice) {
  const currentIndex = themeLibrary.findIndex((theme) => theme.id === themeChoice);
  return themeLibrary[(currentIndex + 1 + themeLibrary.length) % themeLibrary.length]?.id ?? themeLibrary[0].id;
}

export function cssVariableToColor(value: string, alpha?: number) {
  if (alpha === undefined) {
    return `hsl(${value})`;
  }

  return `hsl(${value} / ${alpha})`;
}
